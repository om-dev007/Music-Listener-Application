import mongoose from "mongoose";

const dbSchema = new mongoose.Schema({
    username: {
        type: String,
        required: true,
        unique: true
    },
    email: {
        type: String,
        required: true,
        unique: true,
    },
    password: {
        type: String,
        required: true,
    },
    role: {
        type: String,
        enum: ['user','artist'],
        default: "user"
    }
})

export const userModel = mongoose.model('user', dbSchema)